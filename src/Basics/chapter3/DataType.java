package Basics.chapter3;

public class DataType {
    public static void main(String[] args) {

        // int
        long val = 26L;
        int decVal = 26;
        int hexVal = 0x1a;
        int oxVal = 032;
        int binVal = 0b1110;
        System.out.println(oxVal);

        // float
        float f1 = 123.4F;
        double d1 = 123.4;
        double d2 = 1.234e2;
    }
}
